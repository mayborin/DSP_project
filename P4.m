h=firpm(30,[0 .15 .2 .5]*2, [1 1 0 0],[1 1]); 
[H,w]=freqz(h);
figure(1);
subplot(2,3,1);
stem(h);
title('h[k]');
subplot(2,3,2)
plot(w/pi,abs(H));
title('|H(e^{j\varpi})|');
subplot(2,3,3);
plot(w/pi,20*log10(abs(H)));
title('|H(e^{j\varpi})|_{dB}');
subplot(2,3,4);
plot(w/pi,unwrap(angle(H)));
title('\angleH(e^{j\varpi})');
subplot(2,3,6);
zplane(h);
grid;
title('Zero-distribution');

